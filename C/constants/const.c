#include<stdio.h>

int main(){

    // constant = A fixed value that cannot be altered by an program
    // in order to make a value consant
    // just preceed a datatype with the const keyword
    // const float pi = 3.14159;
    const float PI = 3.14159;

    // a common naming convention for constants is that all the letters are capital letter
    printf("%f", PI);

    return 0;
}